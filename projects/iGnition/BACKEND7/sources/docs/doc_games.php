<blockquote>
Sections allow you to categorize your games database by different types. The most popular use of sections is to divide your games by what platform they are on. You can only assign one section to a game, so if you need to put a game into several sections, you will have to add each one individually.

</blockquote>

<a name="sectionsA"></a><b>Adding Sections</b>
<blockquote>
To add a new section, go to Game Manager -> Add Section.</blockquote>

<a name="sectionsB"></a><b>Editing Sections</b>
<blockquote>
To edit a section, go to Game Manager -> Manage Sections -> Select the section you wish to edit and choose the Edit Section button.</blockquote>

<a name="sectionsC"></a><b>Deleting Sections</b>
<blockquote>
To delete a section, go to Game Manager -> Manage Sections -> Select the section you wish to delete and choose the Delete Section button.</blockquote>


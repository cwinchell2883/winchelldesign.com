function move_menu(num)
{
	switch(num)

	{

	  case 1:window.location="aboutus.php" ;break;

	  case 2:window.location="product.php" ;break;

	  case 3:window.location="services.php" ;break;

	  case 4:window.location="resources.php" ;break;

	  case 5:window.location="casestudy.php" ;break;
	  
	}
}